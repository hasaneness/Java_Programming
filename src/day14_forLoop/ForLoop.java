package day14_forLoop;

public class ForLoop {

    public static void main(String[] args) {

        for( int num =1 ; num <7 ; num +=2){
            System.out.println("number = " + num);
        }

        for( int i = 1; i < 10;  i += 1){ // i ++
            System.out.println("Hello world");
        }


    }


}
