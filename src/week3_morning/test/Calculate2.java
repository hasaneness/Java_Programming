package week3_morning.test;

public class Calculate2 {

    public static void main(String[] args) {

        double a = 5;
        double b = 4;

        System.out.println( a + " + " + b + " = " + (a + b));
        System.out.println( a + " - " + b + " = " + (a - b));
        System.out.println( a + " * " + b + " = " + (a * b));
        System.out.println( a + " / " + b + " = " + (a / b));
        System.out.println( a + " % " + b + " = " + (a % b));







    }
}
