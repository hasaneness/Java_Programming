package day03_variables;

public class PrimitiveDataTypesIntro {
    public static void main(String[] args) {

        //DataType variableName=Data;

        //byte a = "java";  //byte ONLY accepts integer values (-128~127)

        byte a = 20;

        short p = 17500;

        System.out.println("p");

        /*
         If we print p in the double quote we will get the result as a text
         */

        System.out.println(p);
        /*
        If ve print it without using the double quote we can get the result
        as a number
         */
        // salary is $95000
        int s = 95000;

        System.out.println(s);

        //int n = 9999999999;

        long n = 9999999999l;
        System.out.println(n);


        // gpa ia 3.5
        double gpa1 = 3.5;
        System.out.println(gpa1); // double is the preferred data type for decimals

        float gpa2 = 2.5f;
        System.out.println(gpa2);







    }



}
